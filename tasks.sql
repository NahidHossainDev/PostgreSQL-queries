-- Write an SQL query to retrieve the department name and the average salary
--  of employees working in each department. Sort the results by the average 
--  salary in descending order.
SELECT 
    d.department_name, 
    ROUND(AVG(s.salary), 2)
    FROM employees e
    JOIN departments d ON e.department_id = d.department_id
    JOIN salaries s ON s.emp_id = e.emp_id
    GROUP BY d.department_name
    ORDER BY AVG(s.salary) DESC


